import UIKit

extension UIColor {
    // 메인 배경 색상
    class var backgroundColor: UIColor { return UIColor(named: "backgroundColor")! }
    // text-primary
    class var primaryTextColor: UIColor { return UIColor(named: "primaryColor")! }
    // text-secondary
    class var secondaryTextColor: UIColor { return UIColor(named: "secondaryColor")! }
    // text-tertiary
    class var tertiaryTextColor: UIColor { return UIColor(named: "tertiaryColor")! }
}
